    correct_ans = max(a,b,c,d)
    max_value = a 
    if b >= max_value:
        max_value = b 
    if c >= max_value:
        max_value = c
    if d >= max_value:
        max_value = d 
    text = f"test case : {a,b,c,d} : [ correct : {correct_ans} vs {max_value} ]"
    return max_value == correct_ans, text
    
